// While Loop:

let var1 = 0
while (var1 <= 5){
    console.log(var1)
    var1++
}


let myArray = ['flash', "batman", "superman"]

let arr = 0
while (arr < myArray.length) {
    console.log(`Value is \t\t-->\t\t ${myArray[arr]}`);
    arr = arr + 1
}
console.log("\n\n************************************\n\n");


// do-while loop: first execute the step, then check the condition.
let score = 5
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);


